"""## 4) Label: ICU readmission within 48 hours (fallback 30 days)
Outcome at the ICU-stay level: **does the patient return to ICU within ≤48 hours after ICU discharge during the same hospital stay (same HADM_ID)?**
If too few positives, we fallback to **30-day** ICU readmission.

"""

def build_readmit_label(df_stays, window_hours):
    df = df_stays.sort_values(['SUBJECT_ID','HADM_ID','INTIME']).copy()
    df['NEXT_INTIME'] = df.groupby(['SUBJECT_ID','HADM_ID'])['INTIME'].shift(-1)
    y = ((df['NEXT_INTIME'] - df['OUTTIME']).dt.total_seconds()/(3600) <= window_hours).astype(float)
    y = y.fillna(0.0).astype(int)
    return y

