"""## 7) Models & evaluation
We train Logistic Regression and XGBoost. Threshold is chosen on the validation set to maximize F1.

"""

from sklearn.linear_model import LogisticRegression
from sklearn.metrics import roc_auc_score, average_precision_score, precision_recall_curve, confusion_matrix, accuracy_score, f1_score
from xgboost import XGBClassifier
import numpy as np

def evaluate(model, X, y, split):
    proba = model.predict_proba(X)[:,1]
    roc = roc_auc_score(y, proba) if len(np.unique(y)) > 1 else np.nan
    pr  = average_precision_score(y, proba) if len(np.unique(y)) > 1 else np.nan
    print(f'[{split}] ROC-AUC={roc:.3f}  PR-AUC={pr:.3f}  pos_rate={y.mean():.3f}')
    return proba

def best_threshold(y_true, p):
    prec, rec, thr = precision_recall_curve(y_true, p)
    f1s = 2*prec*rec/(prec+rec+1e-9)
    idx = int(np.nanargmax(f1s))
    t = thr[idx-1] if 0 < idx <= len(thr) else 0.5
    return float(t)

def confusion_at_threshold(y_true, p, thr, name):
    yhat = (p >= thr).astype(int)
    tn, fp, fn, tp = confusion_matrix(y_true, yhat).ravel()
    sens = tp/(tp+fn+1e-9)
    spec = tn/(tn+fp+1e-9)
    ppv = tp/(tp+fp+1e-9)
    npv = tn/(tn+fn+1e-9)
    acc = accuracy_score(y_true, yhat)
    f1  = f1_score(y_true, yhat)
    print(f'[{name}] thr={thr:.3f} | sens={sens:.3f} spec={spec:.3f} PPV={ppv:.3f} NPV={npv:.3f} acc={acc:.3f} F1={f1:.3f}')
    return dict(thr=thr, sens=sens, spec=spec, ppv=ppv, npv=npv, acc=acc, f1=f1, tn=tn, fp=fp, fn=fn, tp=tp)

# Logistic Regression
