p_te = evaluate(lr, Xte, y_test,  'TEST  (LR)')
thr = best_threshold(y_val, p_v)
_ = confusion_at_threshold(y_val, p_v, thr, 'VAL (LR)')
_ = confusion_at_threshold(y_test, p_te, thr, 'TEST (LR)')

# XGBoost
