"""### 5.1 Aggregate vitals/labs per ICU stay
We compute mean/min/max/std for each variable.

"""

def aggregate_events(events, mapping, key_col):
    frames = []
    for var, ids in mapping.items():
        sub = events[events['ITEMID'].isin(ids)].copy()
        if sub.empty:
            continue
        grp = sub.groupby(key_col)['VALUENUM']
        agg = pd.concat([
            grp.mean().rename(f'{var}_mean'),
            grp.min().rename(f'{var}_min'),
            grp.max().rename(f'{var}_max'),
            grp.std().rename(f'{var}_std')
        ], axis=1)
        frames.append(agg)
    out = pd.concat(frames, axis=1) if frames else pd.DataFrame()
    out.reset_index(inplace=True)
    return out

